public class P1_3 {
    
    /**
     * prints a face to the console.
     */
    public void printFace() {
        
        //prints hair
        System.out.println("  /////");
        //prints eyes
        System.out.println(" | 0 0 |");
        //prints nose and ears
        System.out.println("(|  ^  |)");
        //prints mouth
        System.out.println(" | [_] |");
        //prints chin
        System.out.println("  -----");
    }
    
}
