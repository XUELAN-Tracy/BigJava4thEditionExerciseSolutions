public class P1_6 {
    
     /**
     * Prints staircase to the console.
     */
    public void printStaircase() {
        
        System.out.println("            +---+");
        System.out.println("            |   |");
        System.out.println("        +---+---+");
        System.out.println("        |   |   |");
        System.out.println("    +---+---+---+");
        System.out.println("    |   |   |   |");
        System.out.println("+---+---+---+---+");
        System.out.println("|   |   |   |   |");
        System.out.println("+---+---+---+---+");
        
    }
    
}
