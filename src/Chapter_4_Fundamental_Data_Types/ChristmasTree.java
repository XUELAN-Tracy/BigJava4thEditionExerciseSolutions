package Chapter_4_Fundamental_Data_Types;

/**
 * Solution to exercise P4.17
 *
 * @author ChrisBerryman
 */
public class ChristmasTree {
    
    /**
     * Returns a Christmas tree in escaped characters.
     *
     * @return a  a Christmas tree string.
     */
    @Override
    public String toString() {
        return "   /\\  \n  /  \\  \n /    \\ \n/      \\\n-------- \n"
                + "  \"  \"  \n"
                + "  \"  \"  \n"
                + "  \"  \"  \n";
                        
    }
    
}
